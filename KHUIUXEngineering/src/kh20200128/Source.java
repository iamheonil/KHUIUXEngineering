// 클래스 정의
// 클래스 : 프로그램을 구성하는 단위(기본 개발 단위)
public class Source {

	// 메인 메소드
	// 메소드는 기능을 뜻함.
	// 메인 메소드는 프로그램의 시작을 뜻함.
	public static void main(String[] args) {
						//	스트링배열	args(아그스?)
		
		// 콘솔창에 출력해주는 sysout // 이런것들을 메소드라 칭함.
		System.out.println("Hello world!");
						// ln -> line 의 약자
	}
	// end main
	// 메인 메소드의 끝(end main) -> 프로그램 종료를 뜻함.

	
} // 클래스 정리가 끝나는 곳 // end class Basic




// 오늘의 단어 Code Review, Agile, Pair Programming, 
// Code Review -> 한 사람이 작성한 코드를 보고 장, 단점을 피드백 해주는 과정
// Agile -> 프로젝트에 따라 팀이 결성, 해체되는 융합조직, 유연하게 업무에 대처하는 형태
// Pair Programming -> 둘이서 한 팀이 되어 코드를 작성하는데 생산력이 떨어질 수 있지만 지식공유, 심리적 안정감, 높은 업무 집중도, 코드 리뷰등을 동시에 진행 할 수 있다.
