package kh20200211_method;

// 설계를 담당한다.



public class Calculator {

	public int sum(int n1, int n2) {
		return n1 + n2;
	}

	public int sub(int n1, int n2) {
		return n1 - n2;
	}

	public int mul(int n1, int n2) {
		return n1 * n2;
	}

	public double div(int n1, int n2) {
		return n1 / n2;
	}

	
}
